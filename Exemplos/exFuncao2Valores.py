#Soma

def somaValores():
    print("Vamos realizar uma soma!")
    valor1 = int(input("Digite o primeiro valor: "))
    valor2 = int(input("Digite o segundo numero: "))
    
    calculo = valor1 + valor2
    
    print("A soma dos valores é: ", calculo)
    

somaValores()

