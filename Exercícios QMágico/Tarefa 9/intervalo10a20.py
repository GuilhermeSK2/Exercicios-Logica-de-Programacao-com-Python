# Programa que lê um número e verifica se ele está no intervalo de 10 a 20 (inclusive).

num = float(input("Digite um número: "))
if 10 <= num <= 20:
    print("O número está no intervalo de 10 a 20.")
else:
    print("O número não está no intervalo de 10 a 20.")
