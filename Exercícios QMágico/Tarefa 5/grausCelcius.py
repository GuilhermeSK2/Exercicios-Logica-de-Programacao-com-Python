# Programa que converte temperatura em Fahrenheight para graus Célsius.

temperaturaFahrenheit = float(input("Insira o valor da temperatura em Fahrenheit: "))

conversao = (temperaturaFahrenheit - 32) * 5 / 9

print("A temperatura em graus célcius", conversao)