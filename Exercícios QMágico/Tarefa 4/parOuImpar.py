# Programa que recebe um número e retorna para o usuário se o numero é Par ou Ímpar.

numero = int(input("Digite um número para verificar se ele é par ou ímpar:"))

if numero % 2 == 0:
    print("O número é par!")
else:
    print("O número é ímpar")