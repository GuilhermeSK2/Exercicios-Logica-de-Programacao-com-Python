# Programa que recebe um número e retorna se é maior ou menor que 10.

numero = int(input("Digite um número para saber se é maior ou menor que 10:"))

if numero > 10:
    print("É MAIOR QUE 10!")
elif numero == 10:
    print("O NÚMERO DIGITADO É 10!")
else:
    print("NÃO É MAIOR QUE 10!")